#!/usr/bin/env bash
# ============================================================================
# log.sh — 日志工具
# ============================================================================
# 提供统一的日志接口，所有 hook 脚本通过 source 引入使用。
# 日志文件: ~/.auto-claude/logs/auto-claude.log
# 日志格式: [timestamp] [LEVEL] [hook_name] message
# ============================================================================

# 日志目录和文件路径（支持 config.env 覆盖）
readonly LOG_DIR="${LOG_DIR:-${HOME}/.auto-claude/logs}"
readonly LOG_FILE="${LOG_FILE:-${LOG_DIR}/auto-claude.log}"

# 当前 hook 名称（由调用方通过环境变量传入，或从 $0 推断）
_LOG_HOOK_NAME="${HOOK_NAME:-$(basename "${BASH_SOURCE[1]:-${0}}" .sh)}"

# ----------------------------------------------------------------------------
# _log_ensure_dir — 确保日志目录存在
# ----------------------------------------------------------------------------
_log_ensure_dir() {
    if [[ ! -d "${LOG_DIR}" ]]; then
        mkdir -p "${LOG_DIR}" 2>/dev/null || true
    fi
}

# ----------------------------------------------------------------------------
# _log_write — 内部日志写入函数
# 参数:
#   $1 — 日志级别 (INFO / WARN / ERROR)
#   $2 — 日志消息
# ----------------------------------------------------------------------------
_log_write() {
    local level="$1"
    local message="$2"
    local timestamp
    timestamp="$(date '+%Y-%m-%d %H:%M:%S')"

    _log_ensure_dir

    # 格式: [timestamp] [LEVEL] [hook_name] message
    local line="[${timestamp}] [${level}] [${_LOG_HOOK_NAME}] ${message}"

    # 写入日志文件（追加模式），失败时静默忽略
    echo "${line}" >> "${LOG_FILE}" 2>/dev/null || true

    # 同时输出到 stderr（方便调试，不干扰 stdout 的 JSON 输出）
    echo "${line}" >&2 2>/dev/null || true
}

# ----------------------------------------------------------------------------
# log_info — 记录 INFO 级别日志
# 参数:
#   $1 — 日志消息
# 用法:
#   log_info "子代理启动，session_id=abc123"
# ----------------------------------------------------------------------------
log_info() {
    _log_write "INFO" "$1"
}

# ----------------------------------------------------------------------------
# log_warn — 记录 WARN 级别日志
# 参数:
#   $1 — 日志消息
# 用法:
#   log_warn "状态文件不存在，将创建新文件"
# ----------------------------------------------------------------------------
log_warn() {
    _log_write "WARN" "$1"
}

# ----------------------------------------------------------------------------
# log_error — 记录 ERROR 级别日志
# 参数:
#   $1 — 日志消息
# 用法:
#   log_error "jq 未安装，无法解析 JSON"
# ----------------------------------------------------------------------------
log_error() {
    _log_write "ERROR" "$1"
}
